    public void updateUserTable(List<Map<String, String>> users) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        // Xóa tất cả dữ liệu cũ
        model.setRowCount(0);

        // Thêm dữ liệu mới vào bảng
        for (Map<String, String> user : users) {
            String masv = user.get("masv");
            String fullname = user.get("fullname");
            String status = user.get("status");
             model.addRow(new Object[]{masv, fullname, status});
        }
    }
